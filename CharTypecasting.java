
public class CharTypecasting {

	public static void main(String[] args) {
		// implicit typecasting
		 char ch = 'A';
		    System.out.println(ch);
		    int num = ch ;
		    System.out.println(num);
		  
		    char ch1 = 'B';
		    System.out.println(ch1);
		    byte num1 = (byte)ch1 ;
		    System.out.println(num1);
		  
		    
		    char ch2 = 'C';
		    System.out.println(ch2);
		    short num2 = (byte) ch2 ;
		   System.out.println(num2);
		  
		    
		    char ch3 = 'D';
		    System.out.println(ch3);
		    long num3 = ch3 ;
		    System.out.println(num3);
		  
		    
		    char ch4 = 'E';
		    System.out.println(ch4);
		    float num4 = ch4 ;
		    System.out.println(num4);
		  
		    
		    char ch5 = 'F';
		    System.out.println(ch5);
		    double num5 = ch5 ;
		    System.out.println(num5);
		  
		    
		 //ExplicitTypecating
		    
		    int word = 80;
		    System.out.println(word);
		    char letter0 = (char) word ;
		    System.out.println(letter0);
		    
		    byte word1 = 81;
		    System.out.println(word1);
		    char letter1 = (char) word1 ;
		    System.out.println(letter1);
		    
		    short word2 = 82;
		    System.out.println(word2);
		    char letter2 = (char) word2 ;
		    System.out.println(letter2);
		    
		    long word3 = 83;
		    System.out.println(word3);
		    char letter3 = (char) word3 ;
		    System.out.println(letter3);
		    
		    float word4 = 85.85f;
		    System.out.println(word4);
		    char letter4 = (char) word4 ;
		    System.out.println(letter4);
		    
		    double word5 = 84.90;
		    System.out.println(word5);
		    char letter5 = (char) word5 ;
		    System.out.println(letter5);
		    
		    
		    
	}

}
